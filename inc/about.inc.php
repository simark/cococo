<h2>à propos</h2>

<p>
<strong>cococo</strong> vous permet d'assurer le suivi de vos dettes interpersonnelles
très facilement. Qu'est-ce qu'une <em>dette interpersonnelle</em>? Supposez que Mireille,
votre bonne amie, vous a prêté 20&nbsp;$ il y a une semaine et que vous venez de lui payer
une bière valant timidement 5&nbsp;$ lors de votre visite hebdomadaire de la taverne locale.
Votre dette envers elle est maintenant de 15&nbsp;$. Un mois plus tard, après avoir
difficultueusement travaillé, vous lui remettez cette somme et elle prétend qu'il manque
5&nbsp;$&nbsp;: qui a raison? Vous, évidemment, mais il est difficile de le prouver étant
donné qu'aucun suivi n'a été assuré.
</p>

<p>
C'est ici que la fonction de <strong>cococo</strong> prend tout son sens&nbsp;: aussitôt
que vous devez de l'argent ou qu'on vous en doit (sans intérêt), entrez cette information
en étant connecté ici et profitez des bénéfices suivants&nbsp;:
</p>

<ul>
	<li>sommaire de tout ce que vous devez et de tout ce qui vous est dû;</li>
	<li>possibilité d'ajouter une description détaillée lors de la création
	d'une dette;</li>
	<li>confirmation par l'autre parti lors de l'ajout d'une dette ou
	d'un remboursement;</li>
	<li>alertes par courriel ou par messagerie texte configurables;</li>
	<li>remboursement facile via PayPal si l'autre parti a configuré un
	tel compte;</li>
	<li>historique infini de toutes les actions exécutées sur la plateforme.</li>
</ul>

<p>
Aussi&nbsp;: ce service est gratuit! Sans oublier que vous profitez inévitablement
de la présence d'une multitude de chauvesouris (pour l'instant, le nombre est infime et
nous nous en excusons).
</p>

